// Builds vendor/crosswalk.json — the exercise-name resolution data that src/resolve.ts
// statically imports — from the sibling openbody-registry checkout (default
// ../openbody-registry, override with OPENBODY_REGISTRY). Mirrors scripts/sync-schema.mjs:
// the published package ships a data snapshot instead of depending on a sibling checkout
// at runtime, and the vendored file is gitignored + regenerated (run automatically
// pre-pack/publish via package.json "prepack"; safe to re-run any time).
//
// Contents (raw, un-normalized — src/resolve.ts owns the one normalization algorithm and
// builds its lookup indexes at runtime, so sync-time and lookup-time can never disagree):
//   aliases  — per-app exact-name → canonical-id tables from crosswalk/<app>.json
//              (null = curated as unmappable; resolver falls back to opaque)
//   registry — [{ id, names }] for every canonical entry in data/exercises.json
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

// Only app alias tables are bundled (dataset crosswalks like free-exercise-db.json are
// registry work-lists, not runtime lookup tables — and would bloat browser bundles).
const APP_SOURCES = ["hevy", "strong", "liftosaur", "fitnotes", "jefit"];

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const registryDir = process.env.OPENBODY_REGISTRY
  ? path.resolve(process.env.OPENBODY_REGISTRY)
  : path.resolve(root, "../openbody-registry");

const exercisesPath = path.join(registryDir, "data/exercises.json");
if (!fs.existsSync(exercisesPath)) {
  console.error(`sync-crosswalk: no registry found at ${exercisesPath} — set OPENBODY_REGISTRY or check out the sibling openbody-registry repo.`);
  process.exit(1);
}

const entries = JSON.parse(fs.readFileSync(exercisesPath, "utf8"));
const ids = new Set(entries.map((e) => e.id));

const aliases = {};
for (const app of APP_SOURCES) {
  const p = path.join(registryDir, "crosswalk", `${app}.json`);
  if (!fs.existsSync(p)) {
    console.error(`sync-crosswalk: missing ${p}`);
    process.exit(1);
  }
  const doc = JSON.parse(fs.readFileSync(p, "utf8"));
  const table = {};
  for (const m of doc.mappings ?? []) {
    if (typeof m.name !== "string") continue;
    if (m.canonical != null && !ids.has(m.canonical)) {
      console.error(`sync-crosswalk: crosswalk/${app}.json maps '${m.name}' to unknown canonical id '${m.canonical}'`);
      process.exit(1);
    }
    table[m.name] = m.canonical ?? null;
  }
  aliases[app] = table;
}

const out = {
  note: "Generated by scripts/sync-crosswalk.mjs from the sibling openbody-registry checkout — do not edit.",
  aliases,
  registry: entries.map((e) => ({ id: e.id, names: e.names ?? [] })),
};

const destDir = path.join(root, "vendor");
fs.mkdirSync(destDir, { recursive: true });
const dest = path.join(destDir, "crosswalk.json");
fs.writeFileSync(dest, JSON.stringify(out, null, 1) + "\n");
console.log(`sync-crosswalk: ${registryDir} -> ${dest} (${entries.length} registry entries, ${APP_SOURCES.map((a) => `${a}: ${Object.keys(aliases[a]).length}`).join(", ")})`);
